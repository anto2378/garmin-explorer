def test_connection():
    """Test the Garmin Connect connection."""
    print("\nğŸ” Testing Garmin Connect connection...")

    try:
        from garminconnect import Garmin

        # Load credentials from .env
        if not os.path.exists(".env"):
            print("âŒ .env file not found. Run setup first.")
            return

        # Simple way to load .env (for this utility only)
        env_vars = {}
        with open(".env", "r") as f:
            for line in f:
                if "=" in line and not line.startswith("#"):
                    key, value = line.strip().split("=", 1)
                    env_vars[key] = value

        email = env_vars.get("GARMIN_EMAIL")
        password = env_vars.get("GARMIN_PASSWORD")

        if not email or not password:
            print("âŒ Credentials not found in .env file.")
            return

        # Test connection
        client = Garmin(email, password)
        client.login()

        # Try to get basic user info
        display_name = client.get_full_name()

        print("âœ… Successfully connected to Garmin Connect!")
        print(f"ğŸ‘¤ Connected as: {display_name}")

    except ImportError:
        print("âŒ garminconnect library not installed. Run: uv sync")
    except Exception as e:
        print(f"âŒ Connection failed: {e}")
        print("Please check your credentials and try again.")


